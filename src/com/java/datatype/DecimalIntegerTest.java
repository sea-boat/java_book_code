package com.java.datatype;

public class DecimalIntegerTest {

	public static void main(String args[]) {
		float f = 200.6f;
		double d = 200.0;
		int i = (int) f;
		int ii = (int) d;
		float ff = (float) i;
		double dd = (double) i;
		System.out.println("i = " + i);
		System.out.println("ii = " + ii);
		System.out.println("ff = " + ff);
		System.out.println("dd = " + dd);
	}

}
