package com.java.datatype;

public class IntegerCarryTest {
	public static void main(String[] args) {
		int a = 0b101;
		int b = 017;
		int c = 10;
		int d = 0x16;
		System.out.println("a = " + a);
		System.out.println("b = " + b);
		System.out.println("c = " + c);
		System.out.println("d = " + d);
	}
}
