package com.java.operator;

public class OperatorIncreaseDecreaseTest {
	public static void main(String args[]) {
		int a = 10;
		int b = 10;
		int c = 10;
		int d = 10;
		System.out.println(a++);
		System.out.println(a);
		System.out.println(++b);
		System.out.println(c--);
		System.out.println(c);
		System.out.println(--d);
	}
}
