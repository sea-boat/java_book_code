package com.java.operator;

public class OperatorAssignmentTest2 {
	public static void main(String[] args) {
		int a = 3;
		a += 2;
		System.out.println("a=" + a);
		a = 3;
		a -= 2;
		System.out.println("a=" + a);
		a = 3;
		a *= 2;
		System.out.println("a=" + a);
		a = 3;
		a /= 2;
		System.out.println("a=" + a);
		a = 3;
		a %= 2;
		System.out.println("a=" + a);
		a = 3;
		a &= 2;
		System.out.println("a=" + a);
		a = 3;
		a |= 2;
		System.out.println("a=" + a);
		a = 3;
		a ^= 2;
		System.out.println("a=" + a);
		a = 3;
		a <<= 2;
		System.out.println("a=" + a);
		a = 3;
		a >>= 2;
		System.out.println("a=" + a);
		a = 3;
		a >>>= 2;
		System.out.println("a=" + a);
	}
}
