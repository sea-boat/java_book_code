package com.java.operator;

public class OperatorBitwiseLogicalTest2 {
	public static void main(String[] args) {
		int a = 7;
		int b = 15;
		boolean c = true;
		boolean d = false;
		System.out.println(a & b);
		System.out.println(a | b);
		System.out.println(c & d);
		System.out.println(c | d);
	}
}
