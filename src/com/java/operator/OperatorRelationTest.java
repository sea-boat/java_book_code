package com.java.operator;

public class OperatorRelationTest {
	public static void main(String[] args) {
		int a = 2;
		int b = 3;
		System.out.println("a>b的结果为" + (a > b));
		System.out.println("a<b的结果为" + (a < b));
		System.out.println("a==b的结果为" + (a == b));
		System.out.println("a!=b的结果为" + (a != b));
		System.out.println("a>=b的结果为" + (a >= b));
		System.out.println("a<=b的结果为" + (a <= b));
	}
}
