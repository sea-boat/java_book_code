package com.java.statement;

public class SwitchStatementTest2 {
	public static void main(String[] args) {
		char c = 'c';
		switch (c) {
		case 'a':
			System.out.println("apple");
			break;
		case 'b':
			System.out.println("banana");
			break;
		case 'c':
			System.out.println("cherry");
			break;
		}
	}
}
