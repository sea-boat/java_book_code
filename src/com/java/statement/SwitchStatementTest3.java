package com.java.statement;

public class SwitchStatementTest3 {
	public static void main(String[] args) {
		int num = 4;
		switch (num) {
		case 3:
			System.out.println("3");
		case 4:
			System.out.println("4");
		case 5:
			System.out.println("5");
		default:
			System.out.println("6");
		}
	}
}
